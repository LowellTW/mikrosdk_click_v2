void accel15_enable_accel ( accel15_t *ctx ) 
{
    uint8_t buf[ 1 ] = { 0 };
    
    accel15_generic_read( ctx, ACCEL15_REG_POWER_CTRL, buf, 1 );
    
    buf[ 0 ] &= ( ~ACCEL15_X_AXIS_EN );
    buf[ 0 ] |= ACCEL15_X_AXIS_EN;
    
    accel15_generic_write( ctx, ACCEL15_REG_POWER_CTRL, buf, 1 );
}

void accel15_cfg_accel ( accel15_t *ctx, uint8_t acc_odr, uint8_t g_range ) 
{
    uint8_t buf[ 1 ] = { 0 };
    
    accel15_generic_read( ctx, ACCEL15_REG_ACCEL_CONFIG, buf, 1 );
    
    buf[ 0 ] &= ( ~ACCEL15_ACCEL_ODR_MSK );
    buf[ 0 ] |= ( acc_odr & ACCEL15_ACCEL_ODR_MSK );
    
    accel15_generic_write( ctx, ACCEL15_REG_ACCEL_CONFIG, buf, 1 );
    
    accel15_generic_read( ctx, ACCEL15_REG_ACCEL_RANGE, buf, 1 );
    
    buf[ 0 ] &= ( ~ACCEL15_ACCEL_RANGE_MSK );
    buf[ 0 ] |= ( g_range & ACCEL15_ACCEL_RANGE_MSK );
    
    accel15_generic_write( ctx, ACCEL15_REG_ACCEL_RANGE, buf, 1 );
}

