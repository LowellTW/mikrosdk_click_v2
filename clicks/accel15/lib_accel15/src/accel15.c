err_t accel15_get_axis_data ( accel15_t *ctx, accel15_axis_t *axis ) 
{
    uint8_t rx_buf[ 6 ] = { 0 };
    uint16_t tmp = 0;
    
    err_t error_flag = accel15_generic_read( ctx, ACCEL15_REG_DATA_8, rx_buf, 6 );
    
    tmp = rx_buf[ 1 ];
    tmp <<= 8;
    tmp |= rx_buf[ 0 ];
    axis->x = ( int16_t ) tmp;
    
    tmp = rx_buf[ 3 ];
    tmp <<= 8;
    tmp |= rx_buf[ 2 ];
    axis->y = ( int16_t ) tmp;
    
    tmp = rx_buf[ 5 ];
    tmp <<= 8;
    tmp |= rx_buf[ 4 ];
    axis->z = ( int16_t ) tmp;
    
    return error_flag;
}

