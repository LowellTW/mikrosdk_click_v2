void accel15_cfg_setup ( accel15_cfg_t *cfg ) 
{
    cfg->scl  = HAL_PIN_NC;
    cfg->sda  = HAL_PIN_NC;
    cfg->sck  = HAL_PIN_NC;
    cfg->miso = HAL_PIN_NC;
    cfg->mosi = HAL_PIN_NC;
    cfg->cs   = HAL_PIN_NC;
    cfg->in2  = HAL_PIN_NC;
    cfg->in1  = HAL_PIN_NC;

    cfg->i2c_speed   = I2C_MASTER_SPEED_STANDARD;
    cfg->i2c_address = ACCEL15_SET_DEV_ADDR_GND;

    cfg->spi_speed   = 100000;
    cfg->spi_mode    = SPI_MASTER_MODE_0;
    cfg->cs_polarity = SPI_MASTER_CHIP_SELECT_POLARITY_ACTIVE_LOW;

    cfg->drv_sel = ACCEL15_DRV_SEL_I2C;
}

void accel15_drv_interface_selection ( accel15_cfg_t *cfg, accel15_drv_t drv_sel ) 
{
    cfg->drv_sel = drv_sel;
}

err_t accel15_init ( accel15_t *ctx, accel15_cfg_t *cfg ) 
{
    ctx->drv_sel = cfg->drv_sel;

    if ( ACCEL15_DRV_SEL_I2C == ctx->drv_sel ) 
    {
        i2c_master_config_t i2c_cfg;

        i2c_master_configure_default( &i2c_cfg );

        i2c_cfg.scl = cfg->scl;
        i2c_cfg.sda = cfg->sda;

        ctx->slave_address = cfg->i2c_address;

        if ( I2C_MASTER_ERROR == i2c_master_open( &ctx->i2c, &i2c_cfg ) ) 
        {
            return I2C_MASTER_ERROR;
        }

        if ( I2C_MASTER_ERROR == i2c_master_set_slave_address( &ctx->i2c, ctx->slave_address ) ) 
        {
            return I2C_MASTER_ERROR;
        }

        if ( I2C_MASTER_ERROR == i2c_master_set_speed( &ctx->i2c, cfg->i2c_speed ) ) 
        {
            return I2C_MASTER_ERROR;
        }

        ctx->read_f  = accel15_i2c_read;
        ctx->write_f = accel15_i2c_write;
    } 
    else 
    {
        spi_master_config_t spi_cfg;

        spi_master_configure_default( &spi_cfg );

        spi_cfg.sck  = cfg->sck;
        spi_cfg.miso = cfg->miso;
        spi_cfg.mosi = cfg->mosi;

        ctx->chip_select = cfg->cs;

        if ( SPI_MASTER_ERROR == spi_master_open( &ctx->spi, &spi_cfg ) ) 
        {
            return SPI_MASTER_ERROR;
        }

        if ( SPI_MASTER_ERROR == spi_master_set_default_write_data( &ctx->spi, DUMMY ) ) 
        {
            return SPI_MASTER_ERROR;
        }

        if ( SPI_MASTER_ERROR == spi_master_set_mode( &ctx->spi, cfg->spi_mode ) ) 
        {
            return SPI_MASTER_ERROR;
        }

        if ( SPI_MASTER_ERROR == spi_master_set_speed( &ctx->spi, cfg->spi_speed ) ) 
        {
            return SPI_MASTER_ERROR;
        }

        spi_master_set_chip_select_polarity( cfg->cs_polarity );
        spi_master_deselect_device( ctx->chip_select );

        ctx->read_f  = accel15_spi_read;
        ctx->write_f = accel15_spi_write;
    }

    digital_in_init( &ctx->in2, cfg->in2 );
    digital_in_init( &ctx->in1, cfg->in1 );

    return ACCEL15_OK;
}

err_t accel15_default_cfg ( accel15_t *ctx ) 
{
    err_t error_flag = ACCEL15_OK;
    uint8_t data_buf[ 128 ] = { 0 };
    uint16_t cnt = 0;
    uint16_t len = 0;
    uint8_t tx_buf[ 1 ] = { 0 };
    if ( ACCEL15_DRV_SEL_SPI == ctx->drv_sel ) 
    {
        tx_buf[ 0 ] = ACCEL15_NV_DISABLE_I2C;
        
        error_flag |= accel15_generic_write( ctx, ACCEL15_REG_NV_CONFIG, tx_buf, 1 );
        accel15_config_delay( );    
    }
    
    tx_buf[ 0 ] = ACCEL15_SOFT_RESET;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_CMD, tx_buf, 1 );
    accel15_config_delay( );

    tx_buf[ 0 ] = ACCEL15_APS_OFF | ACCEL15_FSW_ON;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_POWER_CONF, tx_buf, 1 );
    accel15_config_delay( );
    
    tx_buf[ 0 ] = ACCEL15_INIT_START;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_INIT_CTRL, tx_buf, 1 );
    accel15_config_delay( );
    
    len = sizeof ( bma490l_config_file );
    while ( cnt < len )
    {
        data_buf[ 0 ] = ( ( cnt / 2 ) & 0x0F );
        error_flag |= accel15_generic_write( ctx, ACCEL15_REG_RESERVED_REG_5B, data_buf, 1 );
        data_buf[ 0 ] = ( uint8_t ) ( ( cnt / 2 ) >> 4 );
        error_flag |= accel15_generic_write( ctx, ACCEL15_REG_RESERVED_REG_5C, data_buf, 1 );
        if ( ( len - cnt ) >= 128 ) 
        {
            memcpy ( data_buf, &bma490l_config_file[ cnt ], 128 );
            error_flag |= accel15_generic_write( ctx, ACCEL15_REG_FEATURE_CONFIG, data_buf, 128 );
        }
        else
        {
            memcpy ( data_buf, &bma490l_config_file[ cnt ], len - cnt );
            error_flag |= accel15_generic_write( ctx, ACCEL15_REG_FEATURE_CONFIG, data_buf, len - cnt );
        }
        cnt += 128;
    }
    accel15_config_delay( );
    
    tx_buf[ 0 ] = ACCEL15_INIT_STOP;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_INIT_CTRL, tx_buf, 1 );
    accel15_config_delay( );
    
    tx_buf[ 0 ] = ACCEL15_INT1_IO_CTRL_OUTPUT_EN | ACCEL15_INT1_IO_CTRL_LVL_HIGH;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_INT1_IO_CTRL, tx_buf, 1 );
    accel15_config_delay( );
    
    tx_buf[ 0 ] = ACCEL15_INT_MAP_DATA_INT1_DRDY;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_INT_MAP_DATA, tx_buf, 1 );
    accel15_config_delay( );
    
    accel15_cfg_accel( ctx, ACCEL15_OUTPUT_DATA_RATE_12_5HZ, ACCEL15_ACCEL_RANGE_2G );
    accel15_config_delay( );
    
    tx_buf[ 0 ] = ACCEL15_ACCEL_ON;
    error_flag |= accel15_generic_write( ctx, ACCEL15_REG_POWER_CTRL, tx_buf, 1 );
    accel15_config_delay( );
    return error_flag;
}

