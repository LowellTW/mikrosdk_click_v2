#include "wheatstone.h"

// ------------------------------------------------------------- PRIVATE MACROS 

#define WHEATSTONE_DUMMY 0

// ------------------------------------------------ PUBLIC FUNCTION DEFINITIONS

void wheatstone_cfg_setup ( wheatstone_cfg_t *cfg )
{
    // Communication gpio pins 
    cfg->sck = HAL_PIN_NC;
    cfg->miso = HAL_PIN_NC;
    cfg->mosi = HAL_PIN_NC;
    cfg->cs = HAL_PIN_NC;
    cfg->an = HAL_PIN_NC;

    cfg->spi_speed = 100000; 
    cfg->spi_mode = SPI_MASTER_MODE_0;
    cfg->cs_polarity = SPI_MASTER_CHIP_SELECT_POLARITY_ACTIVE_LOW;
    cfg->resolution = ANALOG_IN_RESOLUTION_DEFAULT; /*< Should leave this by default for portability purposes. 
                                                        Different MCU's have different resolutions. 
                                                        Change only if necessary.*/
    cfg->vref       = WHEATSTONE_VCC_3V3;
}

err_t wheatstone_init ( wheatstone_t *ctx, wheatstone_cfg_t *cfg )
{
    spi_master_config_t spi_cfg;

    spi_master_configure_default( &spi_cfg );

    spi_cfg.sck  = cfg->sck;
    spi_cfg.miso = cfg->miso;
    spi_cfg.mosi = cfg->mosi;

    ctx->chip_select = cfg->cs;

    if ( SPI_MASTER_ERROR == spi_master_open( &ctx->spi, &spi_cfg ) ) 
    {
        return SPI_MASTER_ERROR;
    }

    if ( SPI_MASTER_ERROR == spi_master_set_default_write_data( &ctx->spi, WHEATSTONE_DUMMY ) ) 
    {
        return SPI_MASTER_ERROR;
    }

    if ( SPI_MASTER_ERROR == spi_master_set_mode( &ctx->spi, cfg->spi_mode ) ) 
    {
        return SPI_MASTER_ERROR;
    }

    if ( SPI_MASTER_ERROR == spi_master_set_speed( &ctx->spi, cfg->spi_speed ) ) 
    {
        return SPI_MASTER_ERROR;
    }

    spi_master_set_chip_select_polarity( cfg->cs_polarity );
    spi_master_deselect_device( ctx->chip_select );

    analog_in_config_t adc_cfg;

    analog_in_configure_default( &adc_cfg );

    adc_cfg.input_pin = cfg->an;

    if ( ADC_ERROR == analog_in_open( &ctx->adc, &adc_cfg ) ) 
    {
        return ADC_ERROR;
    }

    if ( ADC_ERROR == analog_in_set_vref_value( &ctx->adc, cfg->vref ) ) 
    {
        return ADC_ERROR;
    }

    if ( ADC_ERROR == analog_in_set_resolution( &ctx->adc, cfg->resolution ) ) 
    {
        return ADC_ERROR;
    }

    return WHEATSTONE_OK;
}

void wheatstone_set_potentiometer ( wheatstone_t *ctx, uint8_t pot_value )
{
    spi_master_select_device( ctx->chip_select );

    spi_master_write( &ctx->spi, &pot_value, 1 );

    spi_master_deselect_device( ctx->chip_select );
    
    float gain_r2_kohm = WHEATSTONE_MAX_POT_KOHM * 
                         ( 1 - ( float ) pot_value / WHEATSTONE_POT_RESOLUTION ) + 
                         WHEATSTONE_R8_KOHM;
    ctx->gain = 1 + gain_r2_kohm / WHEATSTONE_GAIN_R1_KOHM;
}

err_t wheatstone_read_an_pin_value ( wheatstone_t *ctx, uint16_t *data_out ) 
{
    return analog_in_read( &ctx->adc, data_out );
}

err_t wheatstone_read_an_pin_voltage ( wheatstone_t *ctx, float *data_out ) 
{
    return analog_in_read_voltage( &ctx->adc, data_out );
}

